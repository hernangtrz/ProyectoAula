    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        ArchivoDocAsignaturas archivo = new ArchivoDocAsignaturas(); 
        String nombre = txtCodigo.getText();         

        try {
            archivo.eliminarRelacion(nombre);
            txtCodigo.setEnabled(true);
            txtCodigo.setText("");
            txtNomAsignatura.setText("");
            txtNomDocente.setText("");
            btnConsultar.setEnabled(true);
            btnEliminar.setEnabled(false);
            btnCancelar.setEnabled(false);
            txtCodigo.requestFocus();
                        
        } catch (IOException ex) {
            Logger.getLogger(EliminarAsignacionDocente.class.getName()).log(Level.SEVERE, null, ex);
        }

    }//GEN-LAST:event_btnEliminarActionPerformed

