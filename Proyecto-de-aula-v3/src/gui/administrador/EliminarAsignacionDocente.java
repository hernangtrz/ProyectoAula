    private void btnConsultarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConsultarActionPerformed
        String codigo = txtCodigo.getText().toUpperCase();
        
        ArchivoDocAsignaturas aa = new ArchivoDocAsignaturas();
               
        try{
            List<String> relacion = aa.consultaRelacion(codigo);
            if (relacion!=null){                

                txtCodigo.setEnabled(false);
                txtNomAsignatura.setText(relacion.get(1));
                txtNomDocente.setText(relacion.get(2));                
                btnCancelar.setEnabled(true);
                btnConsultar.setEnabled(false);
                btnEliminar.setEnabled(true);
            }   else {

                    txtCodigo.setText("");
                    txtCodigo.requestFocus();
                }
        }   catch(IOException ex){
                
            } 
    }//GEN-LAST:event_btnConsultarActionPerformed

