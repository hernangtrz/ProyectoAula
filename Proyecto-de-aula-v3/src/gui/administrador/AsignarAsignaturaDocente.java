    private void btnAsignarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAsignarActionPerformed
        String codigo = txtCodigo.getText().toUpperCase();
        String nombreA = txtNomAsignatura.getText().toUpperCase();
        String nomDocente = cmbDocentes.getSelectedItem().toString().toUpperCase();
        
        ArchivoDocAsignaturas ada = new ArchivoDocAsignaturas();
        try {
            ada.guardarDocAsignaturas(codigo, nombreA, nomDocente);
            txtCodigo.setText("");
            txtCodigo.setEnabled(true);
            txtNomAsignatura.setText("");
            cmbDocentes.setSelectedIndex(0);
            cmbDocentes.setEnabled(false);
            btnConsultar.setEnabled(true);
            btnAsignar.setEnabled(false);
            btnCancelar.setEnabled(false);
        } catch (IOException ex) {
            Logger.getLogger(AsignarAsignaturaDocente.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }//GEN-LAST:event_btnAsignarActionPerformed

