    private void formWindowActivated(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowActivated
        ArchivoUsuarios archivo = new ArchivoUsuarios(); 

        try {
            List<String> docentes = archivo.informeDocentes();
            
            DefaultComboBoxModel<String> doc = new DefaultComboBoxModel<>(docentes.toArray(new String[0]));
            cmbDocentes.setModel(doc);          
            
        } catch (IOException ex) {
            Logger.getLogger(CrearEliminarAsignatura.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_formWindowActivated

    private void btnConsultarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConsultarActionPerformed
        String codigo = txtCodigo.getText().toUpperCase();
        
        ArchivoAsignaturas aa = new ArchivoAsignaturas();
               
        try{
            Asignatura asignatura = aa.obtenerAsignaturaR(codigo);
            if (asignatura!=null){
                
                txtNomAsignatura.setText(asignatura.getNombre());
                cmbDocentes.setEnabled(true);
                txtCodigo.setEnabled(false);
                btnConsultar.setEnabled(false);
                btnAsignar.setEnabled(true);
                btnCancelar.setEnabled(true);
                
            }   else {
                    JOptionPane.showMessageDialog(null, "Codigo no encontrado !");
                    txtCodigo.setText("");
                    txtCodigo.requestFocus();
                }
        }   catch(IOException ex){
                
            } 
    }//GEN-LAST:event_btnConsultarActionPerformed

