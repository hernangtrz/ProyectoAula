    private void formWindowActivated(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowActivated
        ArchivoUsuarios au = new ArchivoUsuarios();
        DefaultTableModel modeloTabla = new DefaultTableModel();
        
        modeloTabla.addColumn("NOMBRE");
        modeloTabla.addColumn("APELLIDOS");
        modeloTabla.addColumn("USUARIO");
        modeloTabla.addColumn("CONTRASEÃ‘A");
        modeloTabla.addColumn("CORREO E-");
        modeloTabla.addColumn("DEPARTAMENTO");
        modeloTabla.addColumn("TIPO DE USUARIO");
        
        try{
            List<Usuario> es = au.informeEstudiantes();

        for (Usuario estudiante : es) {
            Object[] fila = new Object[]{
                estudiante.getNombre(),
                estudiante.getApellido(),
                estudiante.getNombreUsuario(),
                estudiante.getContrasena(),
                estudiante.getCorreo(),
                estudiante.getDepartamento(),
                estudiante.getTipoUsuario()
            };
            modeloTabla.addRow(fila);
        }

        // Asignar el modelo de tabla a la tabla existente en el JFrame
        table.setModel(modeloTabla);
        
        }   catch(IOException ex){
                
            }
    }//GEN-LAST:event_formWindowActivated

